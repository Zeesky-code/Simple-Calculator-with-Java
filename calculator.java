public class calculator {
    static int addition(int num1, int num2) {
        int sum = num1 + num2;
        return sum;
    }
    static int subtraction(int num1, int num2) {
        int difference = num1 - num2;
        return difference;
    }
    
  
    public static void main(String[] args) {
        System.out.println(addition(2,4));
    }
}